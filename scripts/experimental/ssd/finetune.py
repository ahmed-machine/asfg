class RomaSSDDataset(Dataset):
    """Loads pseudo-label .pt files + corresponding image pairs for RoMa SSD."""

    def __init__(self, label_dir, pairs_dir, roma_size=800, augment=True):
        self.roma_size = roma_size
        self.augment = augment
        self.label_files = sorted(glob.glob(os.path.join(label_dir, "roma_raw_*.pt")))
        self.pairs_dir = pairs_dir
        valid = []
        for lf in self.label_files:
            pair_id = self._extract_pair_id(lf)
            ref_path = os.path.join(pairs_dir, f"{pair_id}_ref.png")
            src_path = os.path.join(pairs_dir, f"{pair_id}_src_perturbed.png")
            if os.path.exists(ref_path) and os.path.exists(src_path):
                valid.append(lf)
        self.label_files = valid

    def _extract_pair_id(self, label_path):
        basename = os.path.basename(label_path)
        parts = basename.replace("roma_raw_", "").replace(".pt", "")
        idx = parts.rfind("_")
        return parts[:idx]

    def __len__(self):
        return len(self.label_files)

    def __getitem__(self, idx):
        label_path = self.label_files[idx]
        pair_id = self._extract_pair_id(label_path)

        label = torch.load(label_path, map_location='cpu', weights_only=False)
        m = torch.as_tensor(label['m'], dtype=torch.float32)
        c = torch.as_tensor(label['c'], dtype=torch.float32)

        ref_path = os.path.join(self.pairs_dir, f"{pair_id}_ref.png")
        src_path = os.path.join(self.pairs_dir, f"{pair_id}_src_perturbed.png")
        ref = cv2.imread(ref_path)
        src = cv2.imread(src_path)

        if self.augment:
            rng = np.random.RandomState()
            ref, src, m, _ = _random_hflip(ref, src, m, None, rng)
            ref = _photometric_augment(ref, np.random.RandomState())
            src = _photometric_augment(src, np.random.RandomState())

        ref = cv2.resize(ref, (self.roma_size, self.roma_size), interpolation=cv2.INTER_AREA)
        src = cv2.resize(src, (self.roma_size, self.roma_size), interpolation=cv2.INTER_AREA)

        ref_t = torch.from_numpy(ref).permute(2, 0, 1).float() / 255.0
        src_t = torch.from_numpy(src).permute(2, 0, 1).float() / 255.0

        return ref_t, src_t, m, c



def roma_ssd_collate(batch):
    refs = torch.stack([b[0] for b in batch])
    srcs = torch.stack([b[1] for b in batch])
    ms = [b[2] for b in batch]
    cs = [b[3] for b in batch]
    return refs, srcs, ms, cs


def _eval_roma(model, loader, device, conf_weight):
    """Evaluate RoMa on a raw-image dataloader without gradients. Returns avg loss."""
    model.eval()
    total_loss = 0.0
    n = 0
    with torch.no_grad():
        for refs, srcs, ms_list, cs_list in loader:
            refs = refs.to(device)
            srcs = srcs.to(device)
            f_A, f_B, f_mv_A, f_mv_B, img_A_lr, img_B_lr = _compute_frozen_features(
                model, refs, srcs, device)
            B_sz = f_mv_A.shape[0]

            f_A_cat = torch.cat(f_A, dim=-1)
            H_A, W_A = f_A_cat.shape[1], f_A_cat.shape[2]
            from align.romav2.matcher import _compute_match_embeddings, _compute_head_preds
            from align.romav2.geometry import get_normalized_grid
            x = get_normalized_grid(B_sz, H_A, W_A)
            x_emb = nn.functional.linear(
                x.reshape(B_sz, H_A * W_A, 2),
                model.matcher.scale * model.matcher.omega
            ).reshape(B_sz, H_A, W_A, -1)
            pos_emb_grid = torch.cat((x_emb.sin(), x_emb.cos()), dim=-1)

            attn_AB_logits, attn_AB, match_emb_AB = _compute_match_embeddings(
                f_A=f_mv_A, f_B=f_mv_B, pos_emb_grid=pos_emb_grid,
                temp=model.matcher.temp, B=B_sz,
                H_A=H_A, W_A=W_A, H_B=H_A, W_B=W_A,
            )
            warp_AB, confidence_AB = _compute_head_preds(
                f_list_A=f_A, match_emb_AB=match_emb_AB, f_mv_A=f_mv_A,
                img_A=img_A_lr, img_B=img_B_lr, head=model.matcher.head,
            )

            loss = torch.tensor(0.0, device=device)
            for i in range(B_sz):
                m_target = ms_list[i].to(device)
                c_target = cs_list[i].to(device)
                if len(m_target) == 0:
                    continue
                warp_i = warp_AB[i]
                H_w, W_w = warp_i.shape[:2]
                px = ((m_target[:, 0] + 1) * 0.5 * (W_w - 1)).clamp(0, W_w - 1).long()
                py = ((m_target[:, 1] + 1) * 0.5 * (H_w - 1)).clamp(0, H_w - 1).long()
                pred_warp = warp_i[py, px].contiguous()
                target_warp = m_target[:, 2:].contiguous()
                warp_loss = F.smooth_l1_loss(pred_warp, target_warp)
                pred_conf = confidence_AB[i][py, px, 0]
                conf_loss = F.mse_loss(pred_conf, c_target)
                loss = loss + warp_loss + conf_weight * conf_loss
            total_loss += loss.item() / max(B_sz, 1)
            n += 1
    model.train()
    for m_mod in model.f.modules():
        if isinstance(m_mod, (nn.BatchNorm2d, nn.LayerNorm)):
            m_mod.eval()
    return total_loss / max(n, 1)


def train_roma_ssd(data_dir, pairs_dir, device, epochs=3, lr=1e-5, batch_size=1,
                   weight_decay=1e-4, conf_weight=0.5, grad_clip=5.0, grad_accum=1,
                   val_every=200, early_stop_patience=3, min_delta=0.001,
                   resume=False, run_dir=None, log_every=10, use_tb=True, trial=None):
    """Train RoMa via SSD. Returns best val_loss."""
    print("Starting RoMa Simple Self-Distillation...", flush=True)
    torch.set_float32_matmul_precision("highest")

    train_full = RomaSSDDataset(data_dir, pairs_dir, augment=True)
    eval_full = RomaSSDDataset(data_dir, pairs_dir, augment=False)
    if len(train_full) == 0:
        print("No RoMa training data found!")
        return float('inf')

    train_ds, val_ds, test_ds = _split_dataset(train_full, eval_full)
    print(f"  {len(train_full)} total -> train={len(train_ds)} val={len(val_ds)} test={len(test_ds)}", flush=True)

    # Load model
    from align.romav2 import RoMaV2
    from align.romav2.device import set_device
    set_device(device)
    print(f"  Loading RoMa model...", flush=True)
    cfg = RoMaV2.Cfg(setting="fast", compile=False)
    model = RoMaV2(cfg)
    model.apply_setting("satast")
    print(f"  RoMa model loaded", flush=True)

    # Freeze heavy pretrained components
    for param in model.f.parameters():
        param.requires_grad = False
    for param in model.matcher.mv_vit.parameters():
        param.requires_grad = False

    # Use raw-image dataloaders — frozen features computed on-the-fly per batch
    # to avoid ~450GB RAM cache (90MB/pair × 5k pairs)
    loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True,
                        num_workers=2, persistent_workers=True,
                        collate_fn=roma_ssd_collate)
    val_loader = DataLoader(val_ds, batch_size=batch_size, shuffle=False,
                            num_workers=0, collate_fn=roma_ssd_collate)
    test_loader = DataLoader(test_ds, batch_size=batch_size, shuffle=False,
                             num_workers=0, collate_fn=roma_ssd_collate)

    trainable = [p for p in model.parameters() if p.requires_grad]
    n_trainable = sum(p.numel() for p in trainable)
    n_frozen = sum(p.numel() for p in model.f.parameters())
    print(f"  Trainable: {n_trainable:,} params (frozen backbone: {n_frozen:,})", flush=True)

    optimizer = optim.AdamW(trainable, lr=lr, weight_decay=weight_decay)
    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs * len(loader))

    # Logger
    logger = None
    if run_dir:
        logger = MetricsLogger(run_dir, "roma", use_tb=use_tb)
        logger.log_config({
            "lr": lr, "weight_decay": weight_decay, "conf_weight": conf_weight,
            "epochs": epochs, "batch_size": batch_size,
            "train_size": len(train_ds), "val_size": len(val_ds), "test_size": len(test_ds),
            "device": str(device), "trainable_params": n_trainable,
            "frozen_params": n_frozen,
        })

    model.train()
    for m in model.f.modules():
        if isinstance(m, (nn.BatchNorm2d, nn.LayerNorm)):
            m.eval()

    best_val_loss = float('inf')
    patience_counter = 0
    start_epoch = 0
    weights_path = 'align/weights/roma_ssd.pth'
    ckpt_path = 'align/weights/roma_ssd_training.pt'

    if resume and os.path.exists(ckpt_path):
        start_epoch, best_val_loss, patience_counter = _load_training_checkpoint(
            ckpt_path, model, optimizer, scheduler, device)
        start_epoch += 1  # resume from next epoch
        print(f"  Resumed from epoch {start_epoch}, best_val={best_val_loss:.5f}", flush=True)

    for epoch in range(start_epoch, epochs):
        total_loss = 0.0
        total_warp_loss = 0.0
        total_conf_loss = 0.0
        n_batches = 0
        epoch_t0 = time.time()

        for refs, srcs, ms_list, cs_list in loader:
            batch_t0 = time.time()
            refs = refs.to(device)
            srcs = srcs.to(device)
            f_A, f_B, f_mv_A, f_mv_B, img_A_lr, img_B_lr = _compute_frozen_features(
                model, refs, srcs, device)
            B_sz = f_mv_A.shape[0]

            # Trainable part of matcher: attention + head (skip frozen backbone + mv_vit)
            f_A_cat = torch.cat(f_A, dim=-1)
            H_A, W_A = f_A_cat.shape[1], f_A_cat.shape[2]
            H_B, W_B = H_A, W_A
            from align.romav2.geometry import get_normalized_grid
            x = get_normalized_grid(B_sz, H_A, W_A)
            x_emb = nn.functional.linear(
                x.reshape(B_sz, H_B * W_B, 2),
                model.matcher.scale * model.matcher.omega
            ).reshape(B_sz, H_B, W_B, -1)
            pos_emb_grid = torch.cat((x_emb.sin(), x_emb.cos()), dim=-1)

            from align.romav2.matcher import _compute_match_embeddings, _compute_head_preds
            attn_AB_logits, attn_AB, match_emb_AB = _compute_match_embeddings(
                f_A=f_mv_A, f_B=f_mv_B, pos_emb_grid=pos_emb_grid,
                temp=model.matcher.temp, B=B_sz,
                H_A=H_A, W_A=W_A, H_B=H_B, W_B=W_B,
            )
            warp_AB, confidence_AB = _compute_head_preds(
                f_list_A=f_A, match_emb_AB=match_emb_AB, f_mv_A=f_mv_A,
                img_A=img_A_lr, img_B=img_B_lr, head=model.matcher.head,
            )

            loss = torch.tensor(0.0, device=device)
            batch_warp = 0.0
            batch_conf = 0.0
            for i in range(B_sz):
                m_target = ms_list[i].to(device)
                c_target = cs_list[i].to(device)

                if len(m_target) == 0:
                    continue

                query_coords = m_target[:, :2]
                target_warp = m_target[:, 2:]

                warp_i = warp_AB[i]
                H_w, W_w = warp_i.shape[:2]
                px = ((query_coords[:, 0] + 1) * 0.5 * (W_w - 1)).clamp(0, W_w - 1).long()
                py = ((query_coords[:, 1] + 1) * 0.5 * (H_w - 1)).clamp(0, H_w - 1).long()
                pred_warp = warp_i[py, px].contiguous()
                target_warp = target_warp.contiguous()

                warp_loss = F.smooth_l1_loss(pred_warp, target_warp)

                conf_i = confidence_AB[i]
                pred_conf = conf_i[py, px, 0]
                conf_loss = F.mse_loss(pred_conf, c_target)

                loss = loss + warp_loss + conf_weight * conf_loss
                batch_warp += warp_loss.item()
                batch_conf += conf_loss.item()

            loss = loss / max(B_sz, 1)

            # Scale loss for gradient accumulation
            (loss / grad_accum).backward()

            loss_val = loss.item()
            total_loss += loss_val
            total_warp_loss += batch_warp / max(refs.shape[0], 1)
            total_conf_loss += batch_conf / max(refs.shape[0], 1)
            n_batches += 1

            # Step optimizer every grad_accum batches
            if n_batches % grad_accum == 0:
                grad_norm = torch.nn.utils.clip_grad_norm_(trainable, grad_clip).item()
                optimizer.step()
                scheduler.step()
                optimizer.zero_grad()
            else:
                grad_norm = 0.0
            batch_time = time.time() - batch_t0

            if n_batches % log_every == 0:
                cur_lr = scheduler.get_last_lr()[0]
                mem = _get_mem_mb(device)
                msg = (f"    [{n_batches}/{len(loader)}] loss={loss_val:.5f} "
                       f"warp={batch_warp:.5f} conf={batch_conf:.5f} "
                       f"grad={grad_norm:.3f} lr={cur_lr:.2e} "
                       f"{batch_time:.2f}s {mem:.0f}MB")
                print(msg, flush=True)

                if logger:
                    logger.log({
                        "type": "batch", "epoch": epoch, "batch": n_batches,
                        "loss": loss_val,
                        "warp_loss": batch_warp / max(refs.shape[0], 1),
                        "conf_loss": batch_conf / max(refs.shape[0], 1),
                        "grad_norm": grad_norm,
                        "lr": cur_lr,
                        "batch_time_s": batch_time,
                        "mem_mb": mem,
                    })

            # Intra-epoch val check
            if val_every and n_batches % val_every == 0:
                val_loss = _eval_roma(model, val_loader, device, conf_weight)
                improved = val_loss < best_val_loss - min_delta
                if improved:
                    best_val_loss = val_loss
                    patience_counter = 0
                    os.makedirs('align/weights', exist_ok=True)
                    torch.save(model.state_dict(), weights_path)
                    print(f"    VAL [{n_batches}] val={val_loss:.5f} [best, saved]", flush=True)
                else:
                    patience_counter += 1
                    print(f"    VAL [{n_batches}] val={val_loss:.5f} "
                          f"[no improvement {patience_counter}/{early_stop_patience}]", flush=True)

                if logger:
                    logger.log({
                        "type": "val_check", "epoch": epoch, "batch": n_batches,
                        "val_loss": val_loss, "best_val_loss": best_val_loss,
                    })

                if trial is not None:
                    trial.report(val_loss, epoch * len(loader) + n_batches)

                if early_stop_patience and patience_counter >= early_stop_patience:
                    print(f"    Early stopping at epoch {epoch+1} batch {n_batches}", flush=True)
                    break

        # Check if inner loop broke for early stopping
        if early_stop_patience and patience_counter >= early_stop_patience:
            break

        # End-of-epoch val check (only if not already checked recently)
        epoch_time = time.time() - epoch_t0
        avg_loss = total_loss / max(n_batches, 1)
        avg_warp = total_warp_loss / max(n_batches, 1)
        avg_conf = total_conf_loss / max(n_batches, 1)

        if not val_every or n_batches % val_every != 0:
            val_loss = _eval_roma(model, val_loader, device, conf_weight)
            improved = val_loss < best_val_loss - min_delta
            if improved:
                best_val_loss = val_loss
                patience_counter = 0
                os.makedirs('align/weights', exist_ok=True)
                torch.save(model.state_dict(), weights_path)

        print(f"  Epoch {epoch+1}/{epochs} — train={avg_loss:.5f} val={val_loss:.5f} "
              f"best={best_val_loss:.5f} warp={avg_warp:.5f} conf={avg_conf:.5f} "
              f"({epoch_time:.0f}s)", flush=True)

        if logger:
            logger.log({
                "type": "epoch_summary", "epoch": epoch,
                "train_loss": avg_loss, "val_loss": val_loss,
                "best_val_loss": best_val_loss,
                "avg_warp_loss": avg_warp, "avg_conf_loss": avg_conf,
                "epoch_time_s": epoch_time,
            })

        # Save training checkpoint for resume
        os.makedirs('align/weights', exist_ok=True)
        _save_training_checkpoint(ckpt_path, model, optimizer, scheduler,
                                  epoch, best_val_loss, patience_counter)
        print(f"  Training checkpoint saved (resumable)", flush=True)

    # Reload best weights for test eval
    if os.path.exists(weights_path):
        model.load_state_dict(torch.load(weights_path, map_location=device))

    test_loss = _eval_roma(model, test_loader, device, conf_weight)
    print(f"  Test loss: {test_loss:.5f} (best val: {best_val_loss:.5f})", flush=True)
    if logger:
        logger.log({"type": "test", "test_loss": test_loss, "best_val_loss": best_val_loss})
    print(f"  Weights at {weights_path}", flush=True)

    if logger:
        logger.close()

    del model, optimizer
    if "mps" in str(device):
        torch.mps.empty_cache()
    elif "cuda" in str(device):
        torch.cuda.empty_cache()

    return best_val_loss


