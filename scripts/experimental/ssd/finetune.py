class SEARAFTSSDDataset(Dataset):
    """Loads SEA-RAFT pseudo-label flow/uncertainty + corresponding image pairs."""

    def __init__(self, label_dir, pairs_dir, crop_size=512, augment=True):
        self.crop_size = crop_size
        self.augment = augment
        self.pairs_dir = pairs_dir
        self.label_files = sorted(glob.glob(os.path.join(label_dir, "searaft_raw_*.pt")))
        valid = []
        for lf in self.label_files:
            pair_id = self._extract_pair_id(lf)
            ref_path = os.path.join(pairs_dir, f"{pair_id}_ref.png")
            src_path = os.path.join(pairs_dir, f"{pair_id}_src_perturbed.png")
            if os.path.exists(ref_path) and os.path.exists(src_path):
                valid.append(lf)
        self.label_files = valid

    def _extract_pair_id(self, label_path):
        return os.path.basename(label_path).replace("searaft_raw_", "").replace(".pt", "")

    def __len__(self):
        return len(self.label_files)

    def __getitem__(self, idx):
        label_path = self.label_files[idx]
        pair_id = self._extract_pair_id(label_path)

        label = torch.load(label_path, map_location='cpu', weights_only=False)
        flow_gt = torch.as_tensor(label['flow_fwd'], dtype=torch.float32)
        uncertainty = torch.as_tensor(label['uncertainty'], dtype=torch.float32)

        ref_path = os.path.join(self.pairs_dir, f"{pair_id}_ref.png")
        src_path = os.path.join(self.pairs_dir, f"{pair_id}_src_perturbed.png")
        ref = cv2.imread(ref_path, cv2.IMREAD_GRAYSCALE)
        src = cv2.imread(src_path, cv2.IMREAD_GRAYSCALE)

        # Random crop
        H, W = ref.shape
        cs = min(self.crop_size, H, W)
        y0 = np.random.randint(0, H - cs + 1) if H > cs else 0
        x0 = np.random.randint(0, W - cs + 1) if W > cs else 0

        ref = ref[y0:y0+cs, x0:x0+cs]
        src = src[y0:y0+cs, x0:x0+cs]
        flow_gt_np = flow_gt.numpy()[y0:y0+cs, x0:x0+cs]

        if self.augment:
            rng = np.random.RandomState()
            ref, src, _, flow_gt_np = _random_hflip(ref, src, None, flow_gt_np, rng)
            ref = _photometric_augment(ref, np.random.RandomState())
            src = _photometric_augment(src, np.random.RandomState())

        ref_t = torch.from_numpy(np.stack([ref]*3, axis=0)).float()
        src_t = torch.from_numpy(np.stack([src]*3, axis=0)).float()
        flow_gt = torch.from_numpy(flow_gt_np.copy()).permute(2, 0, 1).float()

        return ref_t, src_t, flow_gt


def _eval_searaft(model, loader, device, gamma):
    """Evaluate SEA-RAFT on a dataloader without gradients. Returns avg loss."""
    model.eval()
    total_loss = 0.0
    n = 0
    with torch.no_grad():
        for refs, srcs, flow_gts in loader:
            refs = refs.to(device)
            srcs = srcs.to(device)
            flow_gts = flow_gts.to(device)
            output = model(refs, srcs, iters=12, flow_gt=flow_gts, test_mode=False)
            nf_preds = output['nf']
            n_iters = len(nf_preds)
            loss = torch.tensor(0.0, device=device)
            total_weight = 0.0
            for i in range(n_iters):
                weight = gamma ** (n_iters - 1 - i)
                loss = loss + weight * nf_preds[i].mean()
                total_weight += weight
            loss = loss / total_weight  # normalize across iterations
            total_loss += loss.item()
            n += 1
    model.train()
    return total_loss / max(n, 1)


def train_searaft_ssd(data_dir, pairs_dir, device, epochs=3, lr=2e-5, batch_size=2,
                      weight_decay=1e-4, gamma=0.85, grad_clip=5.0, grad_accum=1,
                      val_every=200, early_stop_patience=3, min_delta=0.001,
                      resume=False, run_dir=None, log_every=10, use_tb=True, trial=None):
    """Train SEA-RAFT via SSD. Returns best val_loss."""
    print("Starting SEA-RAFT Simple Self-Distillation...", flush=True)

    train_full = SEARAFTSSDDataset(data_dir, pairs_dir, augment=True)
    eval_full = SEARAFTSSDDataset(data_dir, pairs_dir, augment=False)
    if len(train_full) == 0:
        print("No SEA-RAFT training data found!")
        return float('inf')

    train_ds, val_ds, test_ds = _split_dataset(train_full, eval_full)
    print(f"  {len(train_full)} total -> train={len(train_ds)} val={len(val_ds)} test={len(test_ds)}", flush=True)

    loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True,
                        num_workers=2, persistent_workers=True)
    val_loader = DataLoader(val_ds, batch_size=batch_size, shuffle=False, num_workers=0)
    test_loader = DataLoader(test_ds, batch_size=batch_size, shuffle=False, num_workers=0)

    # Load model
    from align.sea_raft.raft import RAFT as SEARAFT
    from align.flow_refine import _download_sea_raft_weights
    print(f"  Loading SEA-RAFT model...", flush=True)
    model = SEARAFT()
    ckpt_path = _download_sea_raft_weights()
    state_dict = torch.load(ckpt_path, map_location="cpu", weights_only=True)
    if any(k.startswith("module.") for k in state_dict):
        state_dict = {k.replace("module.", "", 1): v for k, v in state_dict.items()}
    model.load_state_dict(state_dict, strict=False)
    model = model.to(device)
    model.train()
    print(f"  SEA-RAFT model loaded", flush=True)

    n_params = sum(p.numel() for p in model.parameters())
    optimizer = optim.AdamW(model.parameters(), lr=lr, weight_decay=weight_decay)
    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs * len(loader))

    # Logger
    logger = None
    if run_dir:
        logger = MetricsLogger(run_dir, "searaft", use_tb=use_tb)
        logger.log_config({
            "lr": lr, "weight_decay": weight_decay, "gamma": gamma,
            "epochs": epochs, "batch_size": batch_size,
            "train_size": len(train_ds), "val_size": len(val_ds), "test_size": len(test_ds),
            "device": str(device), "params": n_params,
        })

    best_val_loss = float('inf')
    patience_counter = 0
    start_epoch = 0
    weights_path = 'align/weights/searaft_ssd.pth'
    ckpt_path = 'align/weights/searaft_ssd_training.pt'

    if resume and os.path.exists(ckpt_path):
        start_epoch, best_val_loss, patience_counter = _load_training_checkpoint(
            ckpt_path, model, optimizer, scheduler, device)
        start_epoch += 1
        print(f"  Resumed from epoch {start_epoch}, best_val={best_val_loss:.5f}", flush=True)

    for epoch in range(start_epoch, epochs):
        total_loss = 0.0
        n_batches = 0
        epoch_t0 = time.time()

        for refs, srcs, flow_gts in loader:
            batch_t0 = time.time()
            refs = refs.to(device)
            srcs = srcs.to(device)
            flow_gts = flow_gts.to(device)

            output = model(refs, srcs, iters=12, flow_gt=flow_gts, test_mode=False)

            nf_preds = output['nf']
            n_iters = len(nf_preds)
            loss = torch.tensor(0.0, device=device)
            total_weight = 0.0
            for i in range(n_iters):
                weight = gamma ** (n_iters - 1 - i)
                loss = loss + weight * nf_preds[i].mean()
                total_weight += weight
            loss = loss / total_weight  # normalize across iterations

            # Scale loss for gradient accumulation
            (loss / grad_accum).backward()

            loss_val = loss.item()
            total_loss += loss_val
            n_batches += 1

            # Step optimizer every grad_accum batches
            if n_batches % grad_accum == 0:
                grad_norm = torch.nn.utils.clip_grad_norm_(model.parameters(), grad_clip).item()
                optimizer.step()
                scheduler.step()
                optimizer.zero_grad()
            else:
                grad_norm = 0.0
            batch_time = time.time() - batch_t0

            if n_batches % log_every == 0:
                cur_lr = scheduler.get_last_lr()[0]
                mem = _get_mem_mb(device)
                msg = (f"    [{n_batches}/{len(loader)}] loss={loss_val:.5f} "
                       f"grad={grad_norm:.3f} lr={cur_lr:.2e} "
                       f"{batch_time:.2f}s {mem:.0f}MB")
                print(msg, flush=True)

                if logger:
                    logger.log({
                        "type": "batch", "epoch": epoch, "batch": n_batches,
                        "loss": loss_val, "grad_norm": grad_norm,
                        "lr": cur_lr, "batch_time_s": batch_time,
                        "mem_mb": mem,
                    })

            # Intra-epoch val check
            if val_every and n_batches % val_every == 0:
                val_loss = _eval_searaft(model, val_loader, device, gamma)
                improved = val_loss < best_val_loss - min_delta
                if improved:
                    best_val_loss = val_loss
                    patience_counter = 0
                    os.makedirs('align/weights', exist_ok=True)
                    torch.save(model.state_dict(), weights_path)
                    print(f"    VAL [{n_batches}] val={val_loss:.5f} [best, saved]", flush=True)
                else:
                    patience_counter += 1
                    print(f"    VAL [{n_batches}] val={val_loss:.5f} "
                          f"[no improvement {patience_counter}/{early_stop_patience}]", flush=True)

                if logger:
                    logger.log({
                        "type": "val_check", "epoch": epoch, "batch": n_batches,
                        "val_loss": val_loss, "best_val_loss": best_val_loss,
                    })

                if trial is not None:
                    trial.report(val_loss, epoch * len(loader) + n_batches)

                if early_stop_patience and patience_counter >= early_stop_patience:
                    print(f"    Early stopping at epoch {epoch+1} batch {n_batches}", flush=True)
                    break

        if early_stop_patience and patience_counter >= early_stop_patience:
            break

        epoch_time = time.time() - epoch_t0
        avg_loss = total_loss / max(n_batches, 1)

        if not val_every or n_batches % val_every != 0:
            val_loss = _eval_searaft(model, val_loader, device, gamma)
            improved = val_loss < best_val_loss - min_delta
            if improved:
                best_val_loss = val_loss
                patience_counter = 0
                os.makedirs('align/weights', exist_ok=True)
                torch.save(model.state_dict(), weights_path)

        print(f"  Epoch {epoch+1}/{epochs} — train={avg_loss:.5f} val={val_loss:.5f} "
              f"best={best_val_loss:.5f} ({epoch_time:.0f}s)", flush=True)

        if logger:
            logger.log({
                "type": "epoch_summary", "epoch": epoch,
                "train_loss": avg_loss, "val_loss": val_loss,
                "best_val_loss": best_val_loss, "epoch_time_s": epoch_time,
            })

        # Save training checkpoint for resume
        os.makedirs('align/weights', exist_ok=True)
        _save_training_checkpoint(ckpt_path, model, optimizer, scheduler,
                                  epoch, best_val_loss, patience_counter)
        print(f"  Training checkpoint saved (resumable)", flush=True)

    # Reload best weights for test eval
    if os.path.exists(weights_path):
        model.load_state_dict(torch.load(weights_path, map_location=device))

    test_loss = _eval_searaft(model, test_loader, device, gamma)
    print(f"  Test loss: {test_loss:.5f} (best val: {best_val_loss:.5f})", flush=True)
    if logger:
        logger.log({"type": "test", "test_loss": test_loss, "best_val_loss": best_val_loss})
    print(f"  Weights at {weights_path}", flush=True)

    if logger:
        logger.close()

    del model, optimizer
    if "mps" in str(device):
        torch.mps.empty_cache()
    elif "cuda" in str(device):
        torch.cuda.empty_cache()

    return best_val_loss


