class MetricsLogger:
    """Writes JSONL metrics + optional TensorBoard scalars."""

    def __init__(self, run_dir, model_name, use_tb=True):
        os.makedirs(run_dir, exist_ok=True)
        self.jsonl_path = os.path.join(run_dir, f"metrics_{model_name}.jsonl")
        self.jsonl_file = open(self.jsonl_path, "a")
        self.model_name = model_name
        self.global_step = 0

        self.tb_writer = None
        if use_tb and _tb_available:
            tb_dir = os.path.join(run_dir, "tb", model_name)
            self.tb_writer = SummaryWriter(log_dir=tb_dir)

    def log(self, metrics: dict):
        metrics["model"] = self.model_name
        metrics["step"] = self.global_step
        self.jsonl_file.write(json.dumps(metrics) + "\n")
        self.jsonl_file.flush()

        if self.tb_writer:
            for k, v in metrics.items():
                if isinstance(v, (int, float)) and k not in ("step", "epoch", "batch"):
                    self.tb_writer.add_scalar(k, v, self.global_step)

        self.global_step += 1

    def log_config(self, config: dict):
        config["type"] = "run_config"
        config["model"] = self.model_name
        config["timestamp"] = datetime.now(timezone.utc).isoformat()
        self.jsonl_file.write(json.dumps(config) + "\n")
        self.jsonl_file.flush()

    def close(self):
        self.jsonl_file.close()
        if self.tb_writer:
            self.tb_writer.close()


def _split_dataset(train_dataset, eval_dataset, train_frac=0.8, val_frac=0.1, seed=42):
    """Split into train (augmented) / val (clean) / test (clean).

    Args:
        train_dataset: dataset with augment=True (used for train split)
        eval_dataset: dataset with augment=False (used for val/test splits)
    """
    from torch.utils.data import Subset
    n = len(train_dataset)
    assert len(eval_dataset) == n
    indices = list(range(n))
    rng = np.random.RandomState(seed)
    rng.shuffle(indices)
    n_train = int(n * train_frac)
    n_val = int(n * val_frac)
    train_idx = indices[:n_train]
    val_idx = indices[n_train:n_train + n_val]
    test_idx = indices[n_train + n_val:]
    return Subset(train_dataset, train_idx), Subset(eval_dataset, val_idx), Subset(eval_dataset, test_idx)


def _save_training_checkpoint(path, model, optimizer, scheduler, epoch, best_val_loss, patience_counter):
    torch.save({
        'model_state_dict': model.state_dict(),
        'optimizer_state_dict': optimizer.state_dict(),
        'scheduler_state_dict': scheduler.state_dict(),
        'epoch': epoch,
        'best_val_loss': best_val_loss,
        'patience_counter': patience_counter,
    }, path)


def _load_training_checkpoint(path, model, optimizer, scheduler, device):
    ckpt = torch.load(path, map_location=device, weights_only=False)
    model.load_state_dict(ckpt['model_state_dict'])
    optimizer.load_state_dict(ckpt['optimizer_state_dict'])
    scheduler.load_state_dict(ckpt['scheduler_state_dict'])
    return ckpt['epoch'], ckpt['best_val_loss'], ckpt['patience_counter']


def _photometric_augment(img, rng):
    """Apply random photometric jitter to a uint8 image (H, W, C) or (H, W).

    Applies brightness, contrast, gamma, and Gaussian noise independently
    to simulate cross-sensor radiometric differences.
    """
    img = img.astype(np.float32)

    # Brightness: +/- 20%
    brightness = rng.uniform(0.8, 1.2)
    img = img * brightness

    # Contrast: +/- 20% around mean
    mean = img.mean()
    contrast = rng.uniform(0.8, 1.2)
    img = (img - mean) * contrast + mean

    # Gamma: 0.7 - 1.4
    gamma = rng.uniform(0.7, 1.4)
    img = np.clip(img, 0, 255)
    img = 255.0 * (img / 255.0) ** gamma

    # Gaussian noise: sigma 3-12
    sigma = rng.uniform(3, 12)
    noise = rng.normal(0, sigma, img.shape).astype(np.float32)
    img = img + noise

    return np.clip(img, 0, 255).astype(np.uint8)


def _random_hflip(ref, src, m, flow, rng):
    """Random horizontal flip with label adjustment.

    Args:
        ref, src: images (H, W, C) or (H, W)
        m: RoMa correspondences (N, 4) in [-1, 1] or None
        flow: optical flow (H, W, 2) or None
        rng: numpy RandomState
    Returns:
        Flipped (ref, src, m, flow) or originals.
    """
    if rng.random() < 0.5:
        return ref, src, m, flow

    ref = np.ascontiguousarray(ref[:, ::-1])
    src = np.ascontiguousarray(src[:, ::-1])

    if m is not None:
        m = m.clone()
        m[:, 0] = -m[:, 0]  # flip x coords in A
        m[:, 2] = -m[:, 2]  # flip x coords in B

    if flow is not None:
        flow = flow[:, ::-1].copy()
        flow[:, :, 0] = -flow[:, :, 0]  # flip horizontal flow component

    return ref, src, m, flow


def _compute_frozen_features(model, refs, srcs, device):
    """Compute frozen backbone + mv_vit features on-the-fly for a batch.

    Returns (f_A, f_B, f_mv_A, f_mv_B, img_A_lr, img_B_lr) on device.
    """
    with torch.no_grad():
        img_A_lr = F.interpolate(refs, size=(model.H_lr, model.W_lr),
                                 mode="bicubic", align_corners=False, antialias=True)
        img_B_lr = F.interpolate(srcs, size=(model.H_lr, model.W_lr),
                                 mode="bicubic", align_corners=False, antialias=True)
        f_A = model.f(img_A_lr)
        f_B = model.f(img_B_lr)

        f_A_cat = torch.cat(f_A, dim=-1)
        f_B_cat = torch.cat(f_B, dim=-1)
        with torch.autocast(device, torch.bfloat16, enabled=True):
            f_mv_AB = model.matcher.mv_vit(
                torch.stack((f_A_cat, f_B_cat), dim=1)
            )["x_norm_patchtokens"]
            B_sz = f_A_cat.shape[0]
            H_A, W_A = f_A_cat.shape[1], f_A_cat.shape[2]
            f_mv_AB = f_mv_AB.reshape(B_sz, 2, H_A, W_A, model.matcher.cfg.dim)

        f_mv_A = f_mv_AB[:, 0].float()
        f_mv_B = f_mv_AB[:, 1].float()

    return f_A, f_B, f_mv_A, f_mv_B, img_A_lr, img_B_lr


def _get_mem_mb(device):
    if "mps" in str(device):
        try:
            return torch.mps.current_allocated_memory() / 1e6
        except Exception:
            return 0
    elif "cuda" in str(device):
        return torch.cuda.memory_allocated() / 1e6
    return 0


